int main(void)
{

  /* USER CODE BEGIN 1 */
  int x, y;
  float bpm;
  uint8_t refresh_delay = 0;
  /* USER CODE END 1 */

  /* Enable I-Cache-------------------------------------------------------------*/
  SCB_EnableICache();

  /* Enable D-Cache-------------------------------------------------------------*/
  SCB_EnableDCache();

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC3_Init();
  MX_DMA2D_Init();
  MX_FMC_Init();
  SDRAM_Init();
  MX_I2C3_Init();
  MX_LTDC_Init();
  MX_RTC_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM5_Init();
  MX_TIM6_Init();

  /* USER CODE BEGIN 2 */
  for(y = 0; y < 240*350; y++)
      Display_layer0[0][y] = Display_layer0[1][y] = Display_layer0[2][y] = Display_layer0[3][y] = 0;
  for(y = 0; y < 272*480; y++)
      Display_layer1[y] = 0;
  initTextBuffer(Display_layer1, 480, 272, 0, 0, Arial12x12, TFT_WHITE, 0x00000001);
  for(x = 0; x < 352; x++)
  {
      Display_layer1[15*480 + x+15] = TFT_WHITE;
      Display_layer1[256*480 + x+15] = TFT_WHITE;
  }
  for(y = 0; y < 240; y++)
  {
      Display_layer1[(y+16)*480 + 15] = TFT_WHITE;
      Display_layer1[(y+16)*480 + 366] = TFT_WHITE;
  }
  setTextPosition(370, 52);
  setTextFont(Arial28x28);
  printf("   bpm");
  setTextPosition(370, 16);
  setTextFont(Tahoma42x37);
  printf("\r%d", 126);
  setTextPosition(0, 0);
  setTextFont(Arial12x12);
  //printf("-");
  TFT_LCD_EN();
  TFT_BL_ON();
  
  //Starting the ECG acquisition
  initEcg();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
      StopTim2();
      SetTim2Counter(500);
      ResetTim2Flag();
      StartTim2();
      if(ecg_refresh_request)
      {
          if(refresh_delay == 0)
          {
              draw_ecg_data(Display_layer0[++current_ecg_display_buffer]);
              HAL_LTDC_SetAddress(&hltdc, (uint32_t) (Display_layer0[current_ecg_display_buffer]), 0);
              current_ecg_display_buffer = (current_ecg_display_buffer + 1) % 4;
          }
          refresh_delay++;
          refresh_delay %= 6;
          ecg_refresh_request--;
      }
      if(bpm_refresh_request)
      {
          setTextPosition(370, 16);
          setTextFont(Tahoma42x37);
          bpm = 60.0f/(beat_pulse_period*0.0005f);
          printf("\r%d    ", (uint32_t) bpm);
          //printf("\r%d", beat_pulse_period);
          bpm_refresh_request = 0;
      }
      while((TIM2->SR & 0x00000001) == 0);
      ADC3->CR2 |= 0x40000000;
  }
  /* USER CODE END 3 */

}

/** System Clock Configuration
*/
void SystemClock_Config(void)
{

  RCC_OscInitTypeDef RCC_OscInitStruct;
  RCC_ClkInitTypeDef RCC_ClkInitStruct;
  RCC_PeriphCLKInitTypeDef PeriphClkInitStruct;

  __PWR_CLK_ENABLE();

  __HAL_PWR_VOLTAGESCALING_CONFIG(PWR_REGULATOR_VOLTAGE_SCALE1);

  RCC_OscInitStruct.OscillatorType = RCC_OSCILLATORTYPE_HSE|RCC_OSCILLATORTYPE_LSE;
  RCC_OscInitStruct.HSEState = RCC_HSE_BYPASS;
  RCC_OscInitStruct.LSEState = RCC_LSE_ON;
  RCC_OscInitStruct.PLL.PLLState = RCC_PLL_ON;
  RCC_OscInitStruct.PLL.PLLSource = RCC_PLLSOURCE_HSE;
  RCC_OscInitStruct.PLL.PLLM = 25;
  RCC_OscInitStruct.PLL.PLLN = 400;
  RCC_OscInitStruct.PLL.PLLP = RCC_PLLP_DIV2;
  RCC_OscInitStruct.PLL.PLLQ = 2;
  HAL_RCC_OscConfig(&RCC_OscInitStruct);

  HAL_PWREx_ActivateOverDrive();

  RCC_ClkInitStruct.ClockType = RCC_CLOCKTYPE_HCLK|RCC_CLOCKTYPE_SYSCLK
                              |RCC_CLOCKTYPE_PCLK1|RCC_CLOCKTYPE_PCLK2;
  RCC_ClkInitStruct.SYSCLKSource = RCC_SYSCLKSOURCE_PLLCLK;
  RCC_ClkInitStruct.AHBCLKDivider = RCC_SYSCLK_DIV1;
  RCC_ClkInitStruct.APB1CLKDivider = RCC_HCLK_DIV4;
  RCC_ClkInitStruct.APB2CLKDivider = RCC_HCLK_DIV2;
  HAL_RCC_ClockConfig(&RCC_ClkInitStruct, FLASH_LATENCY_6);

  PeriphClkInitStruct.PeriphClockSelection = RCC_PERIPHCLK_LTDC|RCC_PERIPHCLK_RTC
                              |RCC_PERIPHCLK_I2C3;
  PeriphClkInitStruct.PLLSAI.PLLSAIN = 192;
  PeriphClkInitStruct.PLLSAI.PLLSAIR = 5;
  PeriphClkInitStruct.PLLSAI.PLLSAIQ = 2;
  PeriphClkInitStruct.PLLSAI.PLLSAIP = RCC_PLLP_DIV2;
  PeriphClkInitStruct.PLLSAIDivQ = 1;
  PeriphClkInitStruct.PLLSAIDivR = RCC_PLLSAIDIVR_4;
  PeriphClkInitStruct.RTCClockSelection = RCC_RTCCLKSOURCE_LSE;
  PeriphClkInitStruct.I2c3ClockSelection = RCC_I2C3CLKSOURCE_PCLK1;
  HAL_RCCEx_PeriphCLKConfig(&PeriphClkInitStruct);

  HAL_RCC_EnableCSS();

  HAL_SYSTICK_Config(HAL_RCC_GetHCLKFreq()/1000);

  HAL_SYSTICK_CLKSourceConfig(SYSTICK_CLKSOURCE_HCLK);

}

